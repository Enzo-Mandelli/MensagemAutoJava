    private String[] geraArray(){
        String[] dados = new String[4];
        dados[0] = mensagem.getText();
        dados[1] = pessoa.getText();
        if(UtilidadesHorario.scheckValid(hora.getText() + ":" + minuto.getText())){
        dados[2] = hora.getText() + ":" + minuto.getText();
        }else{
            dados[2] = "-1:-1";
        }
        dados[3] = buildStringDayWeek();
        return dados;
    }
    
    
    public String[] getGeraArray(){
        return geraArray();
    }
    public int getMessageId() throws IOException, CsvException{
        indexMsg = Telas.select.selectMessage();
        return indexMsg;
    }
    public void getDados(){
        dados = Csv.sreaderCsv(caminho);
        dados = UtillMsg.ordenarPorHorario(dados);
    }
    
    public void setEditData(){
        getDados();
        String aux = String.valueOf(UtilidadesHorario.formataHorario(dados.get(indexMsg)[posHorario])[0]);
        if(Integer.parseInt(aux) < 9 && Integer.parseInt(aux) > -1) aux = "0" + aux;
        mensagem.setText(dados.get(indexMsg)[posMensagem]);
        pessoa.setText(dados.get(indexMsg)[posPessoa]);
        hora.setText(aux);
        aux = String.valueOf(UtilidadesHorario.formataHorario(dados.get(indexMsg)[posHorario])[1]);
        if(Integer.parseInt(aux) < 9 && Integer.parseInt(aux) > -1) aux = "0" + aux;
        minuto.setText(aux);
        decodeHash();
    }
    
    public void decodeHash(){
        String hash = dados.get(indexMsg)[posWeek];
        String[] hashFatiado = hash.split("");
        if(!hashFatiado[0].equals("0")) {
            seg.setSelected(true);
        }else{
            seg.setSelected(false);
        }
        
        if(!hashFatiado[1].equals("0")) {
            ter.setSelected(true);
        }else{
            ter.setSelected(false);
        }
        
        if(!hashFatiado[2].equals("0")) {
            qua.setSelected(true);
        }else{
            qua.setSelected(false);
        }
        
        if(!hashFatiado[3].equals("0")){
            qui.setSelected(true);
        }else{
            qui.setSelected(false);
        }
        
        if(!hashFatiado[4].equals("0")) {
            sex.setSelected(true);
        }else{
            sex.setSelected(false);
        }
        
        if(!hashFatiado[5].equals("0")){
            sab.setSelected(true);
        }else{
            sab.setSelected(false);
        }
        
        if(!hashFatiado[6].equals("0")) {
            dom.setSelected(true);
        }else{
            dom.setSelected(false);
        }
    }
    private String buildStringDayWeek(){
        String build = "";
        if(seg.isSelected()){
            build = build + "1";
        }else{
            build = build + "0";
        }
        if(ter.isSelected()){
            build = build + "2";
        }else{
            build = build + "0";
        }
        if(qua.isSelected()){
            build = build + "3";
        }else{
            build = build + "0";
        }
        if(qui.isSelected()){
            build = build + "4";
        }else{
            build = build + "0";
        }
        if(sex.isSelected()){
            build = build + "5";
        }else{
            build = build + "0";
        }
        if(sab.isSelected()){
            build = build + "6";
        }else{
            build = build + "0";
        }
        if(dom.isSelected()){
            build = build + "7";
        }else{
            build = build + "0";
        }
        return build;
    }
